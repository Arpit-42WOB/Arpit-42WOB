#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	ch;

	ch = (char) c;
	while (*s != '\0' && ch != *s)
		s++;
	if (ch == *s)
		return ((char *)s);
	else
		return (NULL);
}
